#include "push_swap.h"

void	cmd_ra(t_pp *a)
{
	a->l = a->l->next;
	write(1, "ra\n", 3);
}

void	cmd_rb(t_pp *b)
{
	b->l = b->l->next;
	write(1, "rb\n", 3);
}

void	cmd_rr(t_pp *a, t_pp *b)
{
	a->l = a->l->next;
	b->l = b->l->next;
	write(1, "rr\n", 3);
}
